def calcular_amplitud_por_tramo(picos_altos, picos_bajos, y_cm, inicio_s, fin_s, fps, label="Tramo"):
    """
    Calcula la amplitud media (diferencia alto-bajo) de un tramo específico.
    """
    amplitudes = []

    # Convertir a índices de frame
    inicio = int(inicio_s * fps)
    fin = int(fin_s * fps)

    # Asegurarse de que sean arrays enteros
    picos_altos = np.array(picos_altos, dtype=int)
    picos_bajos = np.array(picos_bajos, dtype=int)

    for pa in picos_altos:
        if pa < inicio or pa > fin:
            continue
        bajos_despues = picos_bajos[(picos_bajos > pa) & (picos_bajos <= fin)]
        if len(bajos_despues) > 0:
            pb = bajos_despues[0]
            if pb < len(y_cm) and pa < len(y_cm):
                amp = y_cm.iloc[pb] - y_cm.iloc[pa]
                amplitudes.append(abs(amp))

    amplitud_media = np.mean(amplitudes) if amplitudes else 0

    print(f"--- {label} ---")
    print(f"Amplitud media: {amplitud_media:.2f} cm")
    print()

    return amplitud_media


