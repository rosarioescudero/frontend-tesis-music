"""### Cuantificación del cambio del Amplitud (VIDEO) con Sincronización del Tappeo con Metronomo (Audio)

Esto se puede obviar si es u comportamiento común en todos los pacientes. Pero si puede llegar a ser de interes si un paciente no presenta un comportamiento normal, puede ser un indicador de utilidad

La **correlación de Pearson** es una medida estadística que evalúa la fuerza y dirección de la relación lineal entre dos variables cuantitativas.

Su valor, representado por la letra '\(r\)', varía entre +1 y -1. Un valor cercano a \(+1\) indica una correlación lineal positiva fuerte (ambas variables aumentan juntas), mientras que un valor cercano a \(-1\) señala una correlación negativa fuerte (una variable aumenta mientras la otra disminuye


Pearson te indica si cambian juntas, y en qué sentido:

Si r > 0, cuando aumenta la asincronía también aumenta la amplitud (movimientos más amplios cuando el tap es más impreciso o tardío).

Si r < 0, cuando aumenta la asincronía disminuye la amplitud (la persona ajusta acortando el movimiento para sincronizarse mejor).

Si r ≈ 0, no hay una relación lineal clara entre el tamaño del movimiento y el error temporal.

**Interpretación de los valores de r**

**Valor de r**_____________________________**Interpretación**

+1.0__________________________________Correlación perfecta positiva (las dos variables crecen juntas, perfectamente lineales).

+0.5 a +0.7____________________________Correlación positiva moderada (relación clara, pero con variabilidad).

0_____________________________________No hay relación lineal (puede haber relación no lineal).

-0.5 a -0.7	___________________________Correlación negativa moderada (una variable aumenta mientras la otra disminuye).

-1.0__________________________________Correlación perfecta negativa (una sube exactamente cuando la otra baja).
"""

from scipy.stats import pearsonr

def correlacion_asincronia_amplitud(asynchrony_dict, amplitud_dict, tipo="PR"):
    """
    Calcula y grafica la correlación entre la asincronía y la amplitud promedio por tramo.

    Args:
        asynchrony_dict (dict): Diccionario con arrays de asincronías por tramo.
        amplitud_dict (dict): Diccionario con amplitudes promedio por tramo.
        tipo (str): Tipo de metrónomo ("PA" o "PR")
    """
    tramos = list(asynchrony_dict.keys())
    medias_asynchrony = [np.mean(asynchrony_dict[t]) for t in tramos]
    amplitudes = [amplitud_dict[t] for t in tramos]

    # Calcular correlación de Pearson
    corr, p_value = pearsonr(medias_asynchrony, amplitudes)

    print(f"\n--- Correlación entre Asincronía y Amplitud ({tipo}) ---")
    for t, a, b in zip(tramos, medias_asynchrony, amplitudes):
        print(f"{t}: Asincronía media = {a:.2f} ms | Amplitud media = {b:.2f} cm")
    print(f"\nCoeficiente de correlación: {corr:.3f}")
    print(f"p-valor: {p_value:.4f}")

    # --- Gráfico ---
    plt.figure(figsize=(6, 5))
    plt.scatter(medias_asynchrony, amplitudes, color='royalblue', alpha=0.8, label='Puntos: promedio por tramo')

    # Línea de tendencia
    z = np.polyfit(medias_asynchrony, amplitudes, 1)
    p = np.poly1d(z)
    plt.plot(medias_asynchrony, p(medias_asynchrony), "r--", label='Línea de tendencia (ajuste por mínimos cuadrados)')

    # Etiquetas y título
    plt.title(f"Correlación Asincronía–Amplitud ({tipo})\nCoef. r = {corr:.2f}")
    plt.xlabel("Asincronía media (ms)\n(Mayor valor → tappeo más desfasado del metrónomo)")
    plt.ylabel("Amplitud media (cm)\n(Mayor valor → movimiento de la mano más amplio)")
    plt.grid(True)

    # Leyenda con descripción
    legend_text = (f"r = {corr:.2f}  →  mide la relación entre asincronía y amplitud")
    plt.legend(title=legend_text, loc='best', fontsize=9, title_fontsize=9)

    plt.tight_layout()
    plt.show()

