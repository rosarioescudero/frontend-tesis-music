def calcular_tiempos_por_tramo(picos_altos, picos_bajos, inicio_s, fin_s, fps, label="Tramo"):
    """
    Calcula el tiempo medio de descenso (alto→bajo) y ascenso (bajo→alto)
    dentro de un tramo temporal específico.

    Args:
        picos_altos, picos_bajos : arrays con índices de picos
        inicio_s, fin_s : límites del tramo (en segundos)
        fps : cuadros por segundo
        label : texto descriptivo del tramo

    Returns:
        tuple: (t_desc_medio, t_asc_medio, porc_desc, porc_asc)
    """

    # Convertir a índices
    inicio = int(inicio_s * fps)
    fin = int(fin_s * fps)

    # Asegurarse de que sean arrays numpy
    picos_altos = np.array(picos_altos, dtype=int)
    picos_bajos = np.array(picos_bajos, dtype=int)

    # Filtrar picos dentro del tramo
    picos_altos_tramo = picos_altos[(picos_altos >= inicio) & (picos_altos <= fin)]
    picos_bajos_tramo = picos_bajos[(picos_bajos >= inicio) & (picos_bajos <= fin)]

    t_desc, t_asc = [], []

    # Descenso: de pico alto a pico bajo siguiente
    for pa in picos_altos_tramo:
        pb = picos_bajos_tramo[picos_bajos_tramo > pa]
        if len(pb) > 0:
            tiempo_desc = (pb[0] - pa) * 1000 / fps  # ms
            t_desc.append(tiempo_desc)

    # Ascenso: de pico bajo a pico alto siguiente
    for pb in picos_bajos_tramo:
        pa = picos_altos_tramo[picos_altos_tramo > pb]
        if len(pa) > 0:
            tiempo_asc = (pa[0] - pb) * 1000 / fps  # ms
            t_asc.append(tiempo_asc)

    # Calcular medias y proporciones
    t_desc_medio = np.mean(t_desc) if len(t_desc) else np.nan
    t_asc_medio = np.mean(t_asc) if len(t_asc) else np.nan

    suma = np.nanmean(t_desc) + np.nanmean(t_asc) if len(t_desc) and len(t_asc) else np.nan
    porc_desc = (t_desc_medio / suma * 100) if suma and not np.isnan(suma) else np.nan
    porc_asc = (t_asc_medio / suma * 100) if suma and not np.isnan(suma) else np.nan

    print(f"--- {label} ---")
    print(f"Tiempo medio de descenso ↓: {t_desc_medio:.2f} ms")
    print(f"Tiempo medio de ascenso ↑:  {t_asc_medio:.2f} ms")
    print(f"Porcentaje de descenso: {porc_desc:.2f}% | ascenso: {porc_asc:.2f}%\n")

    return t_desc_medio, t_asc_medio, porc_desc, porc_asc

