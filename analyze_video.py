"""### Calculos de asincronia entre tappeo y metronomo

Para comparar la señal del tapping con el metronomo, se debe calcular la  asincronia se compone un algortimo que tiene en cuenta lo siguiente:


1.   La asincronia se define como la diferencia de tiempo entre un pitido (pico del metronomo) y el tappeo (pico del tapping) que realiza el paciente al oirlo o intuirlo, ya que a veces hay un retraso y a veces un adelanto por parte del paciente.
2.   Teniendo en cuenta que el paciente puede no tappear al sonar un pitido, existe cierto tiempo o una cantidad pitidos de resincronizacion.
3.   Como no todos los pitidos se corresponden con un tappeo, no se puede comparar las listas que contienen los picos sin un ajuste. Para obtener un valor preciso de la asincronia, se compara cada uno de los tappeos con el pitido que mas cercano que se encuentre.
4.   Se asume como pitido mas cercano aquel cuya asincronia con el tappeo sea menor a match_ms. Este valor corresponde a la mitad del periodo del metronomo. Esto supone que un tappeo se realiza, como maximo, con un retraso (o adelanto) de medio pulso respecto del pitido, es decir, para un bpm = 120, match_ms = 250ms.

#### ISO
"""

# En caso de ser ISO
if metadata['tipo_metronomo'] == 'ISO':
  asynchrony_ms = []
  metronomo_peaks_matched = []
  tapping_peaks_matched = []
  # Copia temporal de los picos del metrónomo para ir eliminando los emparejados
  metronomo_peaks_temp = list(metronomo_peaks)

  # Itera sobre cada pico de tapping
  for tapping_peak in tapping_peaks:

    # Si no hay picos de metronomo para emparejar, sal del bucle
    if not metronomo_peaks_temp:
        break

    # Calcula la diferencia de tiempo entre el tappeo y todos los picos de metronomo restantes
    diferencias = np.abs(tapping_peak - np.array(metronomo_peaks_temp))

    # Encuentra el índice del pico del metronomo más cercano
    indice_cercano = np.argmin(diferencias)

    # Obtén el pico de metronomo más cercano y su diferencia en milisegundos
    metronomo_peak_cercano = metronomo_peaks_temp[indice_cercano]
    asynchrony_samples = (tapping_peak - metronomo_peak_cercano) * 1000 / sample_rate

    match_ms = (60/metadata['bpm'])*(1000)/2

    # Asume que una diferencia mayor a la mitad de un pulso no es una coincidencia
    if abs(asynchrony_samples) < match_ms:
        asynchrony_ms.append(asynchrony_samples)
        metronomo_peaks_matched.append(metronomo_peak_cercano)
        tapping_peaks_matched.append(tapping_peak)
        # Remueve el pico del metronomo ya emparejado
        del metronomo_peaks_temp[indice_cercano]

  #   Definicion de variables para la funcion de fenerar grafico
  asynchrony_ms = np.array(asynchrony_ms)
