    df_landmarks['vector_A_y'] = df_landmarks['promedio_tip_y'] - df_landmarks['promedio_mcp_y']
    df_landmarks['vector_B_x'] = df_landmarks['promedio_mcp_x'] - df_landmarks['muñeca_x']
    df_landmarks['vector_B_y'] = df_landmarks['promedio_mcp_y'] - df_landmarks['muñeca_y']

    # Producto punto y magnitudes
    dot_product = (df_landmarks['vector_A_x'] * df_landmarks['vector_B_x']) + (df_landmarks['vector_A_y'] * df_landmarks['vector_B_y'])
    magnitude_A = np.sqrt(df_landmarks['vector_A_x']**2 + df_landmarks['vector_A_y']**2)
    magnitude_B = np.sqrt(df_landmarks['vector_B_x']**2 + df_landmarks['vector_B_y']**2)

    denominator = np.where(magnitude_A * magnitude_B == 0, 1e-10, magnitude_A * magnitude_B)
    cosine_angle = np.clip(dot_product / denominator, -1.0, 1.0)

    df_landmarks['angulo_postura'] = np.degrees(np.arccos(cosine_angle))
    return df_landmarks


