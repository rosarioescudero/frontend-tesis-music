def bpm_to_ms(bpm):
    return (60 / bpm) * 1000

def calcular_tiempo_rampa(bpm_inicial, bpm_final, clics_rampa=10):
    """
    Calcula la duración total de la sección de rampa (transición) en segundos
    sumando los tiempos de los beats interpolados.
    """

    tiempo_rampa_ms = 0
    divisor = clics_rampa - 1

    if divisor <= 0:
        return 0.0

    for i in range(clics_rampa):
        # 1. Calcular el BPM actual con interpolación lineal
        bpm_current = bpm_inicial + (bpm_final - bpm_inicial) * (i / divisor)

        # 2. Calcular la duración del beat actual en milisegundos
        ms_per_beat = bpm_to_ms(bpm_current)

        # 3. Sumar al tiempo total
        tiempo_rampa_ms += ms_per_beat

    # Convertir el resultado final a segundos
    return tiempo_rampa_ms / 1000.0

