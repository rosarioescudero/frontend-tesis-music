def analizar_relacion_angulo_asincronia(df_tip, fps, tiempos_metro, asynchrony_ms, tramo_label):
    if len(asynchrony_ms) == 0 or len(tiempos_metro) == 0:
        print(f"No hay datos suficientes para el tramo {tramo_label}.")
        return None

    # Convertir tiempos a índices de frame
    frame_times = (tiempos_metro * fps).astype(int)
    frame_times = frame_times[frame_times < len(df_tip)]

    angulos_correspondientes = df_tip['angulo_postura'].iloc[frame_times].values
    tiempos_seg = tiempos_metro[:len(angulos_correspondientes)]

    # Correlación entre asincronía y ángulo
    min_len = min(len(asynchrony_ms), len(angulos_correspondientes))
    corr = np.corrcoef(asynchrony_ms[:min_len], angulos_correspondientes[:min_len])[0, 1]

    # Tendencia temporal del ángulo (adaptación)
    slope, intercept, r_value, p_value, std_err = linregress(tiempos_seg, angulos_correspondientes)
    tendencia = "disminuye" if slope < 0 else "aumenta"

    # Variabilidad y medias
    varianza_ang = np.var(angulos_correspondientes)
    mean_ang = np.mean(angulos_correspondientes)
    mean_async = np.mean(asynchrony_ms)

    # Gráfico temporal del ángulo
    plt.figure(figsize=(6, 4))
    plt.scatter(tiempos_seg, angulos_correspondientes, alpha=0.6, label='Ángulo medido', color='royalblue')
    plt.plot(tiempos_seg, intercept + slope * tiempos_seg, color='orange', label='Tendencia lineal')
    plt.title(f'Evolución del ángulo ({tramo_label})')
    plt.xlabel('Tiempo (s)')
    plt.ylabel('Ángulo de Postura (°)')
    plt.grid(True)
    plt.legend()
    plt.tight_layout()
    plt.show()

    # Gráfico relación Ángulo–Asincronía
    plt.figure(figsize=(5, 4))
    plt.scatter(asynchrony_ms[:min_len], angulos_correspondientes[:min_len], alpha=0.7)
    plt.axvline(x=np.mean(asynchrony_ms), color='r', linestyle='--', label='Media Asincronía')
    plt.axhline(y=mean_ang, color='g', linestyle='--', label='Media Ángulo')
    plt.title(f'Ángulo vs Asincronía - {tramo_label}')
    plt.xlabel('Asincronía (ms)')
    plt.ylabel('Ángulo (°)')
    plt.grid(True)
    plt.legend()
    plt.tight_layout()
    plt.show()

    return {
        'tramo': tramo_label,
        'media_asincronia': mean_async,
        'media_angulo': mean_ang,
        'varianza_angulo': varianza_ang,
        'pendiente_angulo': slope,
        'tendencia': tendencia,
        'correlacion_ang_async': corr
    }

