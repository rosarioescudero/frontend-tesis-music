"""## Señal 'ideal'
La señal de movimiento "ideal" sirve para comparar el desempeño de un sujeto con un modelo perfecto y controlado. Es una forma poderosa de analizar visual y cuantitativamente el rendimiento.

Se construye esta señal ideal creando una función que modele el movimiento esperado de la mano, basándose en el ritmo del metrónomo y una amplitud ideal definida.

Cómo Construir la Señal de Movimiento "Ideal"
Una señal de movimiento ideal debe ser una onda suave y repetitiva que esté perfectamente sincronizada con los clics del metrónomo. La forma más simple y efectiva de modelar esto es usando una onda sinusoidal (o cosinusoidal).

**Sincronización:** Una onda sinusoidal es la opción perfecta porque sus picos y valles ocurren a intervalos precisos y predecibles. Puedes diseñarla para que sus picos coincidan exactamente con los clics del metrónomo.

**Amplitud:** Puedes establecer la amplitud de esta onda a un valor consistente e ideal (por ejemplo, la amplitud promedio del 'tappeo' del participante o un máximo predefinido) para que sirva como punto de referencia para la comparación.

**Desplazamiento vertical (Offset):** La señal necesita un desplazamiento vertical para que su valor mínimo se asiente en la posición de "tappeo" sobre la mesa, que corresponde a la posición más baja en tu eje Y.

Aquí tienes un desglose de los componentes matemáticos que necesitarás para crear esta señal ideal:

**Frecuencia (f):** Es la velocidad del movimiento en ciclos por segundo (Hertz). Se determina directamente por el tempo del metrónomo. Por ejemplo, un metrónomo a 60 BPM (pulsos por minuto) corresponde a una frecuencia de 1 Hz. La fórmula es f=
60
BPM
​
 .

**Amplitud (A):** Es la mitad del rango total de movimiento, desde la posición de reposo hasta el punto más alto del 'tappeo'. Puedes calcular esto tomando el promedio de los Picos altos y los Picos bajos de tu señal.

**Desplazamiento (C):** Es la posición vertical del centro de la onda. Puedes calcularlo como el promedio de los Picos altos y los Picos bajos.

**Fase (ϕ):** Esto determina el punto de partida de la onda. Para alinear los picos de la onda sinusoidal con los clics del metrónomo, necesitarás ajustar la fase. Una onda sinusoidal estándar comienza en 0, pero querrás que comience en su valor máximo, por lo que se requiere un desfase de π/2 (o 90 grados).

La ecuación final para tu señal ideal será:

y(t)=A⋅sin(2πft+ϕ)+C
"""

def generar_senal_tappeo_ideal(metadata: dict, df_landmarks: pd.DataFrame, fps: float,
                                 tip_peaks_a: list, tip_peaks_b: list):
    """
    Genera una señal helicoidal que representa un movimiento de tappeo ideal.

    Args:
        metadata (dict): Diccionario con la metadata, incluyendo 'tipo_metronomo',
                         'bpm' (para ISO), o 'bpm_1', 'clics_1', 'bpm_2', 'clics_2' (para PA),
                         o 'bpm_1', 'clics_1', 'clics_rampa', 'clics_2', 'bpm_2' (para PR).
        df_landmarks (pd.DataFrame): DataFrame con los datos de landmarks, incluyendo 'frame'.
        fps (float): Los fotogramas por segundo del video.
        tip_peaks_a (list): Lista de valores Y de los picos superiores del tappeo real.
        tip_peaks_b (list): Lista de valores Y de los picos inferiores (valles) del tappeo real.

    Returns:
        pd.DataFrame: Un DataFrame con las coordenadas 'ideal_x' e 'ideal_y' de la señal helicoidal ideal.
                      Devuelve un DataFrame vacío si hay un error.
    """
    # --- 1. Obtener parámetros comunes y verificar datos ---
    if 'frame' not in df_landmarks.columns:
        print("Error: La columna 'frame' no se encuentra en el DataFrame de landmarks.")
        return pd.DataFrame()

    num_frames_actual_df = len(df_landmarks)

    if not isinstance(tip_peaks_a, np.ndarray) or not isinstance(tip_peaks_b, np.ndarray):
        print("Error: Los picos deben ser arrays de NumPy.")
        return pd.DataFrame()

    if tip_peaks_a.size == 0 or tip_peaks_b.size == 0:
        print("Error: No se detectaron picos o valles de tappeo. No se puede generar la señal ideal.")
        return pd.DataFrame()

    # Calcular el tiempo de desfase (offset)
    t_offset = 3 / fps

    # --------------------- 3. Generar la señal ideal según el tipo de metrónomo (ISO/PA/PR) ------------------------------------
    #----------------------------------------------------------------------------------------------------------------------------

    ### --------------------------------------------Señal ideal con [Metrónomo ISO]----------------------------------------------

    # Asegurarse de que los índices están dentro de los límites del DataFrame
    valid_peaks_a = tip_peaks_a[tip_peaks_a < len(df_landmarks)]
    valid_peaks_b = tip_peaks_b[tip_peaks_b < len(df_landmarks)]

    picos_y_tappeo = df_landmarks['tip_y'].iloc[valid_peaks_a].values
    valles_y_tappeo = df_landmarks['tip_y'].iloc[valid_peaks_b].values

    if picos_y_tappeo.size == 0 or valles_y_tappeo.size == 0:
        print("Error: Los índices de los picos no corresponden a valores en el DataFrame.")
        return pd.DataFrame()

    avg_pico_y = np.mean(picos_y_tappeo)
    avg_valle_y = np.mean(valles_y_tappeo)

    amplitud_y = (avg_pico_y - avg_valle_y) / 2
    desplazamiento_y = avg_valle_y + amplitud_y

    # El radio de la hélice será la amplitud del movimiento de tappeo
    radio_helice = amplitud_y/4

    x_center = np.mean(df_landmarks['tip_x'])

    # --- 2. Generar la señal ideal según el tipo de metrónomo ---

    tipo_metronomo = metadata.get('tipo_metronomo', 'ISO')

    if tipo_metronomo == 'ISO':
        if 'bpm' not in metadata:
            print("Error: 'bpm' no se encuentra en metadata para metrónomo ISO.")
            return pd.DataFrame()

        bpm_actual = metadata['bpm']
        frecuencia_hz = bpm_actual / 60.0

        # El número total de vueltas de la hélice se basa en la duración y frecuencia
        total_vueltas = frecuencia_hz * (num_frames_actual_df / fps)

        tiempo = np.arange(num_frames_actual_df) / fps


        # Generamos la señal helicoidal
        ideal_y = amplitud_y * np.cos(2 * np.pi * frecuencia_hz * (tiempo - t_offset)) + desplazamiento_y
        ideal_x = radio_helice * np.sin(2 * np.pi * frecuencia_hz * (tiempo - t_offset)) + x_center

        senal_ideal_df = pd.DataFrame({'ideal_x': ideal_x, 'ideal_y': ideal_y})


        ### --------------------------------------------Señal ideal con [Metrónomo PA]----------------------------------------------

    elif tipo_metronomo == 'PA':
        required_keys_pa = ['bpm_1', 'clics_1', 'bpm_2', 'clics_2']
        if not all(key in metadata for key in required_keys_pa):
            print(f"Error: Faltan claves en metadata para metrónomo PA. Se requieren: {required_keys_pa}")
            return pd.DataFrame()

        bpm_1 = metadata['bpm_1']
        clics_1 = metadata['clics_1']
        bpm_2 = metadata['bpm_2']
        clics_2 = metadata['clics_2']

        # Segmento 1
        periodo_1 = 60.0 / bpm_1
        duracion_s_1_ideal = clics_1 * periodo_1
        num_frames_1_ideal = int(duracion_s_1_ideal * fps)

        # Segmento 2
        periodo_2 = 60.0 / bpm_2
        duracion_s_2_ideal = clics_2 * periodo_2
        num_frames_2_ideal = int(duracion_s_2_ideal * fps)

        if (num_frames_1_ideal + num_frames_2_ideal) > num_frames_actual_df:
            num_frames_2_ideal = num_frames_actual_df - num_frames_1_ideal
            if num_frames_2_ideal < 0:
                print("Advertencia: La duración ideal del segmento 1 excede la duración real del video.")
                return pd.DataFrame()

        # Generar las señales helicoidales para cada segmento
        tiempo_1 = np.arange(num_frames_1_ideal) / fps
        tiempo_1 = tiempo_1 - t_offset

        frecuencia_hz_1 = bpm_1 / 60.0
        ideal_y_1 = amplitud_y * np.cos(2 * np.pi * frecuencia_hz_1 * tiempo_1) + desplazamiento_y
        ideal_x_1 = radio_helice * np.sin(2 * np.pi * frecuencia_hz_1 * tiempo_1) + x_center

        tiempo_2 = np.arange(num_frames_2_ideal) / fps
        tiempo_2 = tiempo_2 - t_offset

        frecuencia_hz_2 = bpm_2 / 60.0
        ideal_y_2 = amplitud_y * np.cos(2 * np.pi * frecuencia_hz_2 * tiempo_2) + desplazamiento_y
        ideal_x_2 = radio_helice * np.sin(2 * np.pi * frecuencia_hz_2 * tiempo_2) + x_center

        # Concatenar las señales
        ideal_x = np.concatenate((ideal_x_1, ideal_x_2))
        ideal_y = np.concatenate((ideal_y_1, ideal_y_2))

        # Asegurar que el tamaño de la señal ideal coincida con el DataFrame original
        if len(ideal_x) < num_frames_actual_df:
            padding_x = np.full(num_frames_actual_df - len(ideal_x), ideal_x[-1])
            padding_y = np.full(num_frames_actual_df - len(ideal_y), ideal_y[-1])
            ideal_x = np.concatenate((ideal_x, padding_x))
            ideal_y = np.concatenate((ideal_y, padding_y))
        elif len(ideal_x) > num_frames_actual_df:
            ideal_x = ideal_x[:num_frames_actual_df]
            ideal_y = ideal_y[:num_frames_actual_df]

        senal_ideal_df = pd.DataFrame({'ideal_x': ideal_x, 'ideal_y': ideal_y})

        ### --------------------------------------------Señal ideal con [Metrónomo PR]----------------------------------------------

    elif tipo_metronomo == 'PR':
        required_keys_pr = ['bpm_1', 'clics_1', 'clics_rampa', 'clics_2', 'bpm_2']
        if not all(key in metadata for key in required_keys_pr):
            print(f"Error: Faltan claves en metadata para metrónomo PR. Se requieren: {required_keys_pr}")
            return pd.DataFrame()

        bpm_1 = metadata['bpm_1']
        clics_1 = metadata['clics_1']
        clics_rampa = metadata['clics_rampa']
        clics_2 = metadata['clics_2']
        bpm_2 = metadata['bpm_2']

        # --- Cálculo de frames para cada segmento ---
        periodo_1 = 60.0 / bpm_1
        duracion_s_1 = clics_1 * periodo_1
        num_frames_1 = int(duracion_s_1 * fps)

        duracion_s_rampa = calcular_tiempo_rampa(bpm_1, bpm_2, clics_rampa)
        num_frames_rampa = int(duracion_s_rampa * fps)

        periodo_2 = 60.0 / bpm_2
        duracion_s_2 = clics_2 * periodo_2
        num_frames_2 = int(duracion_s_2 * fps)

        # --- Segmento 1: Frecuencia Constante ---
        frecuencia_hz_1 = bpm_1 / 60.0
        tiempo_1 = (np.arange(num_frames_1) / fps) - t_offset
        fase_1 = 2 * np.pi * frecuencia_hz_1 * tiempo_1
        ideal_y_1 = amplitud_y * np.cos(fase_1) + desplazamiento_y
        ideal_x_1 = radio_helice * np.sin(fase_1) + x_center

        # Guardamos la fase final para dar continuidad
        fase_final_1 = fase_1[-1] if num_frames_1 > 0 else 0

        # --- Segmento 2: Rampa de Frecuencia ---
        tiempo_rampa = (np.arange(num_frames_rampa) / fps)

        # Frecuencia instantánea que cambia linealmente con el tiempo
        frec_instantanea_hz = (metadata['bpm_1'] / 60.0) + \
                              ( (metadata['bpm_2'] - metadata['bpm_1']) / 60.0) * (tiempo_rampa / duracion_s_rampa)

        # La fase es la integral de la frecuencia angular (2*pi*f)
        # Integral de (a*t + b) es (a/2)*t^2 + b*t
        f_b = 2 * np.pi * (metadata['bpm_1'] / 60.0)
        f_a = 2 * np.pi * (metadata['bpm_2'] - metadata['bpm_1']) / (60.0 * duracion_s_rampa)

        fase_rampa = (f_a / 2) * tiempo_rampa**2 + f_b * tiempo_rampa + fase_final_1

        ideal_y_rampa = amplitud_y * np.cos(fase_rampa) + desplazamiento_y
        ideal_x_rampa = radio_helice * np.sin(fase_rampa) + x_center

        # Guardamos la fase final para dar continuidad
        fase_final_rampa = fase_rampa[-1] if num_frames_rampa > 0 else fase_final_1

        # --- Segmento 3: Frecuencia Constante ---
        frecuencia_hz_2 = bpm_2 / 60.0
        tiempo_2 = (np.arange(num_frames_2) / fps)
        fase_2 = (2 * np.pi * frecuencia_hz_2 * tiempo_2) + fase_final_rampa
        ideal_y_2 = amplitud_y * np.cos(fase_2) + desplazamiento_y
        ideal_x_2 = radio_helice * np.sin(fase_2) + x_center

        # --- Concatenar y ajustar tamaño ---
        ideal_x = np.concatenate(tuple(filter(lambda arr: arr.size > 0, (ideal_x_1, ideal_x_rampa, ideal_x_2))))
        ideal_y = np.concatenate(tuple(filter(lambda arr: arr.size > 0, (ideal_y_1, ideal_y_rampa, ideal_y_2))))

        # Asegurar que el tamaño coincida con el DataFrame original
        if len(ideal_x) < num_frames_actual_df:
            padding = np.full(num_frames_actual_df - len(ideal_x), ideal_x[-1] if len(ideal_x) > 0 else x_center)
            ideal_x = np.concatenate((ideal_x, padding))
            padding = np.full(num_frames_actual_df - len(ideal_y), ideal_y[-1] if len(ideal_y) > 0 else desplazamiento_y)
            ideal_y = np.concatenate((ideal_y, padding))
        elif len(ideal_x) > num_frames_actual_df:
            ideal_x = ideal_x[:num_frames_actual_df]
            ideal_y = ideal_y[:num_frames_actual_df]

        senal_ideal_df = pd.DataFrame({'ideal_x': ideal_x, 'ideal_y': ideal_y})

    else:
        print(f"Error: Tipo de metrónomo '{tipo_metronomo}' no reconocido.")
        return pd.DataFrame()

    return senal_ideal_df

