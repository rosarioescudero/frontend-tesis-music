"""# Detección y eliminación de las secciones inicial y final

Se aprovecha el patrón de los picos y el silencio que se produce al inicio. El doble clic es un evento único y fuerte, mientras que el metrónomo es una serie de eventos periódicos. El silencio entre el clic y el metrónomo es la clave.

Detectar el Silencio: Después del pico del doble clic, busca un periodo de silencio. Un "silencio" se puede definir como un tramo donde los valores de f se mantienen por debajo de un umbral muy bajo (cercano a cero) durante una duración mínima.

Detectar el Patrón Periódico: Después del periodo de silencio, busca una serie de picos que tengan una periodicidad constante.

La función find_peaks puede encontrar estos picos, y luego se puede analizar la distancia entre ellos. La distancia entre los picos del metrónomo debería ser relativamente constante, mientras que la distancia entre el doble clic y el primer golpe del metrónomo no lo será.

## Recorte de secciones

*   Para el inicio:

En lugar de buscar la primera secuencia periódica, se puede buscar la secuencia periódica más larga o la que tenga la periodicidad más estable. Un metrónomo, por su naturaleza, tendrá una secuencia de picos mucho más larga y consistente que cualquier patrón accidental de ruido.

Identificar el Inicio: El inicio del metrónomo será el primer pico en una serie de picos que sigan un patrón rítmico.




*  Para el final:

**Amplitud absoluta:** Se toma cada muestra de audio y se saca el valor absoluto. Esto sirve porque el audio oscila en positivo y negativo alrededor de cero, y queremos medir su magnitud sin importar el signo.

**Energia en una ventana:** Se suma las amplitudes absolutas en un pedazo de tiempo (50ms).

**ㅤN**

**E=∑​∣xi​∣** donde 𝑥𝑖 son las muestras de audio en la ventana y 𝑁 es la cantidad de muestras en ese intervalo.

**ㅤi=1**

Con esto se detecta cuando hay un golpe fuerte (ej. el paciente golpeando la mesa), la energía en esa ventana sube mucho. Y cuando no hay nada (silencio o ruido muy leve), la energía cae a valores cercanos a cero.

Cuando la energía es menor al 10% de la máxima energía se corta el audio.
"""

def _calcular_tiempo_rampa(bpm1, bpm2, n_clics):
    """Función auxiliar para calcular la duración de una rampa de metrónomo."""
    if n_clics <= 1:
        return 0.0
    bpms = np.linspace(bpm1, bpm2, n_clics)
    periodos_entre_clics = 60.0 / bpms[:-1]
    return np.sum(periodos_entre_clics)

def find_rhythmic_segment(audio_data, sample_rate, metadata,
                          back_search_s=2.0, smooth_ms=10, min_run_peaks=3):
    """
    Detección robusta de INICIO y FIN del patrón rítmico.
    Válido para 'ISO', 'PA' y 'PR'.
    Devuelve (start_index, end_index)
    """

    # --- (1. Setup: envolvente, picos, etc. - Sin cambios) ---
    audio = np.array(audio_data)
    if audio.ndim > 1: audio = np.mean(audio, axis=1)
    if audio.size == 0: return 0, len(audio_data)
    max_abs = np.max(np.abs(audio));
    if max_abs == 0: return 0, len(audio_data)
    audio_n = audio / max_abs
    smooth_win = max(1, int(sample_rate * smooth_ms / 1000.0))
    env = np.convolve(np.abs(audio_n), np.ones(smooth_win)/smooth_win, mode='same')

    tipo = metadata.get('tipo_metronomo', 'ISO')
    if tipo == 'PA' or tipo == 'PR':
        bpm_ref = metadata.get('bpm_1', metadata.get('bpm', 120))
    else:
        bpm_ref = metadata.get('bpm', 120)
    period_samples = int(round(sample_rate * 60.0 / float(bpm_ref)))

    distance = max(1, int(0.5 * period_samples))
    tol = max(int(0.40 * period_samples), 1)
    h = np.percentile(env, 70)
    prom = 0.15 * np.max(env)

    peaks, props = find_peaks(env, height=h, distance=distance, prominence=prom)
    for pct in (60, 50, 40):
        if len(peaks) >= min_run_peaks: break
        h = np.percentile(env, pct)
        prom = max(0.05 * np.max(env), 0.1 * prom)
        peaks, props = find_peaks(env, height=h, distance=distance, prominence=prom)

    if len(peaks) < min_run_peaks:
        return 0, len(audio_data)
    peaks = np.sort(peaks)

    # --- (2. Lógica de INICIO - Sin cambios) ---
    inicio_ritmo = peaks[0]
    diffs = np.diff(peaks)
    best_start=None; best_len=0; run_start=None; run_len=0
    for i, d in enumerate(diffs):
        if abs(d - period_samples) <= tol:
            if run_start is None: run_start = i; run_len = 1
            else: run_len += 1
        else:
            if run_len > best_len: best_len = run_len; best_start = run_start
            run_start=None; run_len = 0
    if run_len > best_len: best_len = run_len; best_start = run_start
    if best_start is not None: inicio_ritmo = peaks[best_start]

    search_start = max(0, inicio_ritmo - int(back_search_s * sample_rate))
    segmento = env[search_start: inicio_ritmo + 1]
    if segmento.size > 0:
        local_thr = np.percentile(segmento, 60)
        pseg, _ = find_peaks(segmento, height=local_thr, distance=max(1, int(0.3*period_samples)))
        if len(pseg) > 0: inicio_ritmo = int(search_start + pseg[-1])

    # --- (3. LÓGICA DE FIN - MODIFICADA PARA AGRUPAR 'PA' Y 'PR') ---

    fin_ritmo = peaks[-1] # Fallback por defecto

    # AHORA TRATAMOS PA Y PR JUNTOS
    if tipo == 'PR' or tipo == 'PA':
        try:
            # 3a. Calcular la duración ideal total del metrónomo
            bpm_1 = metadata['bpm_1']
            clics_1 = metadata['clics_1']
            bpm_2 = metadata['bpm_2']
            clics_2 = metadata['clics_2']

            duracion_s_1 = (60.0 / bpm_1) * clics_1
            duracion_s_2 = (60.0 / bpm_2) * clics_2

            # El tiempo de rampa es 0 para 'PA'
            duracion_s_rampa = 0.0
            if tipo == 'PR':
                clics_rampa = metadata['clics_rampa']
                duracion_s_rampa = _calcular_tiempo_rampa(bpm_1, bpm_2, clics_rampa)

            duracion_total_ideal = duracion_s_1 + duracion_s_rampa + duracion_s_2

            # 3b. Calcular el índice final MÍNIMO permitido (el "ancla")
            fin_minimo = inicio_ritmo + int(duracion_total_ideal * sample_rate)

            # 3c. Definir ventana de "rescate" (basada en el BPM más lento)
            periodo_lento = 60.0 / min(bpm_1, bpm_2)
            ventana_rescate = int(2.5 * periodo_lento * sample_rate)
            fin_busqueda_max = fin_minimo + ventana_rescate

            # 3d. Encontrar el último pico real
            picos_candidatos = peaks[peaks >= fin_minimo]
            picos_validos = picos_candidatos[picos_candidatos <= fin_busqueda_max]

            if len(picos_validos) > 0:
                fin_ritmo = picos_validos[-1]
            else:
                fin_ritmo = fin_minimo

        except KeyError as e:
            print(f"Advertencia: Falta metadata 'PA' o 'PR' ({e}). Usando el último pico como fallback.")
            fin_ritmo = peaks[-1]

    else: # ESTE BLOQUE AHORA ES SOLO PARA 'ISO'
        is_rhythmic = np.abs(np.diff(peaks) - period_samples) <= tol
        required_run_len = min_run_peaks - 1
        kernel = np.ones(required_run_len, dtype=int)
