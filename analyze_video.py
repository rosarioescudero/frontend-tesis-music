def extract_metadata_from_filename(filename):
    """
    Extrae metadatos del nombre del archivo usando expresiones regulares,
    manejando diferentes nomenclaturas de metrónomo (ISO, PR, PA).
    """
    filename = os.path.basename(filename or "")
    filename = os.path.splitext(filename)[0]
    filename = filename.replace('ï¿½', '_').replace('\ufffd', '_')
    filename = re.sub(r'^pista_\d+_', '', filename, flags=re.IGNORECASE)
    metadata = {}

    # --- Patrón para metrónomos ISO ---
    pattern_iso = r'(S\d+)_([A-Z]+)_ISO(\d+)_([A-Za-z]+\d+)'
    match_iso = re.search(pattern_iso, filename)
    if match_iso:
        metadata['sujeto'] = re.sub(r'\D', '', match_iso.group(1))
        metadata['mano'] = match_iso.group(2)
        metadata['tipo_metronomo'] = 'ISO'
        metadata['bpm'] = int(match_iso.group(3))
        metadata['medicion'] = re.sub(r'\D', '', match_iso.group(4))
        return metadata

    # --- Patrón para metrónomos Abruptos (PA) ---
    pattern_pa = r'(S\d+)_([A-Z]+)_(PA)([+-]\d+)__(\d+)-(\d+)_([A-Za-z]+\d+)'
    match_pa = re.search(pattern_pa, filename)
    if match_pa:
        metadata['sujeto'] = re.sub(r'\D', '', match_pa.group(1))
        metadata['mano'] = match_pa.group(2)
        metadata['tipo_metronomo'] = 'PA'
        porcentaje_cambio = int(match_pa.group(4)) # Corrected group index for percentage change
        bpm_2 = 120 * (1 + porcentaje_cambio / 100)
        metadata['porcentaje_cambio'] = porcentaje_cambio
        metadata['bpm_1'] = 120
        metadata['bpm_2'] = round(bpm_2)
        metadata['clics_1'] = int(match_pa.group(5))
        metadata['clics_2'] = int(match_pa.group(6))
        metadata['medicion'] = re.sub(r'\D', '', match_pa.group(7))
        return metadata

    # --- Patrón para metrónomos de Rampa (PR) ---
    pattern_pr = r'(S\d+)_([A-Z]+)_(PR)([+-]\d+)__(\d+)-(\d+)-(\d+)-([A-Za-z]+\d+)'
    match_pr = re.search(pattern_pr, filename)
    if match_pr:
        metadata['sujeto'] = re.sub(r'\D', '', match_pr.group(1))
        metadata['mano'] = match_pr.group(2)
        metadata['tipo_metronomo'] = 'PR'
        porcentaje_cambio = int(match_pr.group(4))
        bpm_2 = 120 * (1 + porcentaje_cambio / 100)

        metadata['porcentaje_cambio'] = porcentaje_cambio
        metadata['bpm_1'] = 120
        metadata['bpm_2'] = round(bpm_2)
        metadata['clics_1'] = int(match_pr.group(5))
        metadata['clics_rampa'] = int(match_pr.group(6))
        metadata['clics_2'] = int(match_pr.group(7))
        metadata['medicion'] = re.sub(r'\D', '', match_pr.group(8))
        return metadata

    return {}

