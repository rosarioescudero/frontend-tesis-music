def generar_csv_sincronia(audio_data, sr, taps_video_tiempos):
    # 1. Detectar Beats del Audio (Estímulo)
    onset_frames = librosa.onset.onset_detect(y=audio_data, sr=sr, backtrack=True)
    beat_tiempos = librosa.frames_to_time(onset_frames, sr=sr)

    registros = []
    ventana_ms = 0.3  # Ventana de 300ms para emparejar

    for beat_t in beat_tiempos:
        # Buscamos el tap más cercano
        diferencias = np.abs(taps_video_tiempos - beat_t)

        if len(diferencias) > 0 and np.min(diferencias) < ventana_ms:
            idx_mejor_tap = np.argmin(diferencias)
            tap_t = taps_video_tiempos[idx_mejor_tap]
            asincronia = (tap_t - beat_t) * 1000 # Convertimos a ms

            registros.append({
                'Evento': 'Sincronizado',
                'Tiempo_Beat_s': round(float(beat_t), 3),
                'Tiempo_Tap_s': round(float(tap_t), 3),
                'Asincronia_ms': round(float(asincronia), 2)
            })
            # Eliminamos el tap usado para el uno-a-uno
            taps_video_tiempos = np.delete(taps_video_tiempos, idx_mejor_tap)
